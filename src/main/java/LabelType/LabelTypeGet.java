package LabelType;


public class LabelTypeGet {
    String reference;
    String s;
    
    
    public LabelTypeGet(String reference, String s) {
        this.reference = reference;
        this.s = s;
    }

    public LabelTypeGet() {
    }

    public String getReference() {
        return reference;
    }

    public String getS() {
        return s;
    }

    public void setReference(String reference) {
        this.reference = reference;
    }

    public void setS(String s) {
        this.s = s;
    }
    
    
}

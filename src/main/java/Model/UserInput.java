package Model;

class UserInput {
    String code;
    String value;

    public UserInput() {
    }

    public UserInput(String code, String value) {
        this.code = code;
        this.value = value;
    }

    public String getCode() {
        return code;
    }

    public String getValue() {
        return value;
    }

    public void setCode(String code) {
        this.code = code;
    }

    public void setValue(String value) {
        this.value = value;
    }
    
          
}
